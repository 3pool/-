# 1.载入库
import turtle

# 2.创建绘制图像的画笔对象
t = turtle.Pen()

# 3.设置画笔对象的参数,设置画笔绘制的速度，值越小速度越慢
t.speed(1)

# 4.开始绘制
# 让画笔向前绘制
t.forward(100)

# 让画笔向左绘制
t.left(90)

# 让画笔向前绘制
t.forward(100)

# 让画笔向左绘制
t.left(90)

# 让画笔向前绘制
t.forward(100)

# 让画笔向左绘制
t.left(90)

# 让画笔向前绘制
t.forward(100)

# 让画笔向左绘制
t.left(90)

# 停止操作，等待响应
turtle.down()

